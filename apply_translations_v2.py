def apply():
    if not os.path.exists(po_path):
        print(f"Error: {po_path} not found")
        return

    with open(po_path, 'r', encoding='utf-8') as f:
        content = f.read()

    # Step 1: Update existing untranslated msgids
    # We use simple string replacement to avoid regex issues with large blocks
    for english, arabic in translations.items():
        english_escaped = english.replace('\\', '\\\\').replace('"', '\\"')
        arabic_escaped = arabic.replace('\\', '\\\\').replace('"', '\\"')
        
        # Pattern for active untranslated entry
        pattern = f'msgid "{english_escaped}"\nmsgstr ""'
        replacement = f'msgid "{english_escaped}"\nmsgstr "{arabic_escaped}"'
        content = content.replace(pattern, replacement)
        
        # Also try with \r\n just in case
        pattern_rn = f'msgid "{english_escaped}"\r\nmsgstr ""'
        replacement_rn = f'msgid "{english_escaped}"\r\nmsgstr "{arabic_escaped}"'
        content = content.replace(pattern_rn, replacement_rn)

    # Step 2: Add missing msgids
    # We append them to the end of the file if they don't exist as active
    missing_entries = []
    for english, arabic in translations.items():
        english_escaped = english.replace('\\', '\\\\').replace('"', '\\"')
        if f'msgid "{english_escaped}"' not in content:
            msg = f'\nmsgid "{english_escaped}"\nmsgstr "{arabic.replace("\\", "\\\\").replace("\"", "\\\"")}"\n'
            missing_entries.append(msg)
    
    if missing_entries:
        print(f"Adding {len(missing_entries)} missing entries...")
        content += "\n" + "\n".join(missing_entries)

    with open(po_path, 'w', encoding='utf-8') as f:
        f.write(content)

    print("Successfully updated django.po")
    
    # Step 3: Compile messages
    try:
        import subprocess
        print("Compiling messages...")
        result = subprocess.run(['python', 'manage.py', 'compilemessages'], capture_output=True, text=True)
        if result.returncode == 0:
            print("Successfully compiled messages!")
        else:
            print(f"Error compiling messages: {result.stderr}")
    except Exception as e:
        print(f"Failed to run compilemessages: {e}")

if __name__ == "__main__":
    apply()
